class AddressModel{
    constructor(addressID, userID, province, district, ward, street, fullAddress, phone, createTime, updateTime){
        this.addressID = addressID;
        this.userID = userID;
        this.province = province;
        this.district = district;
        this.ward = ward;
        this.street = street;
        this.fullAddress = fullAddress;
        this.phone = phone;
        this.createTime = createTime;
        this.updateTime = updateTime;
    }
}